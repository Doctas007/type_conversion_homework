# Task 5: Add After Conversion
# Convert string numbers to integers and add them

str_num1 = "10"
str_num2 = "20"

# Convert to integers
num1 = int(str_num1)
num2 = int(str_num2)

# Add the numbers
sum_result = num1 + num2

print(f"First number: {num1}")
print(f"Second number: {num2}")
print(f"Sum: {sum_result}")
